#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s)
{
	char	*copy;

	copy = (char *) malloc (ft_strlen(s) * sizeof(char) + 1);
	if (copy == NULL)
		return (NULL);
	ft_strcpy(copy, s);
	return (copy);
}
